/** 获取指定年级+学科的课纲约束文本（用于 AI 出题提示词） */
export function getCurriculumScope(grade: Grade | null | undefined, subject: Subject): string {
  const parts: string[] = [];

  // 数学课纲（精细化）
  if (subject === "MATH" && grade) {
    const mathScope = MATH_CURRICULUM[grade];
    if (mathScope) {
      parts.push(mathScope);
    } else {
      // 小学数学（PRIMARY）— 通用约束
      parts.push(`【小学数学·出题约束】
- 仅考查四则运算、分数、小数、简单几何（周长/面积/体积）、统计图表
- 禁止：方程、负数、字母表示数（除特别注明）`);
    }
  }

  // 其他学科通用约束
  const subjectConstraint = SUBJECT_GLOBAL_CONSTRAINTS[subject];
  if (subjectConstraint && subject !== "MATH") {
    parts.push(subjectConstraint);
  } else if (subject !== "MATH") {
    parts.push(`出题时请严格遵守该学段教材的知识范围，不得出现超纲内容。`);
  }

  // 年级补充说明
  if (grade) {
    const gradeName = GRADE_NAMES[grade];
    if (gradeName) {
      parts.unshift(`当前学生年级：${gradeName}`);
    }
  }

  return parts.join("\n\n");
}

const GRADE_NAMES: Record<Grade, string> = {
  PRIMARY_1: "小学一年级",
  PRIMARY_2: "小学二年级",
  PRIMARY_3: "小学三年级",
  PRIMARY_4: "小学四年级",
  PRIMARY_5: "小学五年级",
  PRIMARY_6: "小学六年级",
  JUNIOR_1:  "初一（七年级）",
  JUNIOR_2:  "初二（八年级）",
  JUNIOR_3:  "初三（九年级）",
  SENIOR_1:  "高一",
  SENIOR_2:  "高二",
  SENIOR_3:  "高三",
};
