/** 获取指定年级+学科的课纲约束文本（用于 AI 出题提示词） */
export function getCurriculumScope(grade: Grade | null | undefined, subject: Subject): string {
  const parts: string[] = [];

  if (grade) {
    parts.push(`当前学生年级：${GRADE_NAMES[grade]}`);
  }

  if (subject === "MATH" && grade) {
    const scope = MATH_CURRICULUM[grade];
    if (scope) {
      parts.push(scope);
    } else {
      // 小学数学
      parts.push(`【小学数学 · 出题约束】
只考查：四则运算（整数/小数/分数）、简单几何（周长/面积/体积公式直接应用）、比与比例、简单统计图表
禁止：方程（除简单算式填空）、负数、字母表示数`);
    }
  } else {
    const constraint = SUBJECT_GLOBAL_CONSTRAINTS[subject];
    parts.push(constraint || `出题请严格遵守该学段教材的知识范围，不得出现超纲内容。`);
  }

  return parts.join("\n\n");
}

